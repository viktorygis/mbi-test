// Минимальное число явно проявленных паттернов для диагностики
const MIN_YAPP = 6;

// Главная функция для определения типа реагирования
export function determineResponseType(userPatternsInput) {
    // 1. Формируем список ЯПП, нормализуем
    const userPatterns = (userPatternsInput || []).map((pat) => (pat || "").trim().toLowerCase());

    // 2. Проверка количества ЯПП
    if (userPatterns.length < MIN_YAPP) return "Не определён";

    // 3. Проверка по категориям
    const categoriesCovered = CATEGORIES.every((cat) => userPatterns.some((pat) => cat.patterns.includes(pat)));
    if (!categoriesCovered) return "Не определён";

    // 4. Счётчики баллов по типам
    const typePoints = {
        Моноактивный: 0,
        Мультиактивный: 0,
        Реактивный: 0,
    };

    // 5. Одиночные паттерны (1 балл)
    userPatterns.forEach((pat) => {
        const types = patternToResponseType[pat];
        if (types) {
            types.forEach((type) => {
                typePoints[type]++;
            });
        }
    });

    // 6. Сочетания (2 балла)
    patternCombinations.forEach((comb) => {
        if (comb.elements.every((elem) => userPatterns.includes(elem))) {
            comb.types.forEach((type) => {
                typePoints[type] += 2;
            });
        }
    });

    // 7. Подсчёт баллов, перевод в проценты
    const totalPoints = Object.values(typePoints).reduce((a, b) => a + b, 0);
    if (!totalPoints) return "Не определён";
    const percents = Object.fromEntries(Object.entries(typePoints).map(([type, pts]) => [type, (pts / totalPoints) * 100]));

    // 8. Проверка на "Смешанный"
    const percentValues = Object.values(percents);
    const expressedTypes = percentValues.filter((v) => v > 0).length;
    if (expressedTypes >= 3) {
        const min = Math.min(...percentValues);
        const max = Math.max(...percentValues);
        // 30-40% и разница не больше 15% — смешанный
        if (percentValues.every((v) => v >= 30 && v <= 40) && max - min <= 15) {
            return "Смешанный";
        }
    }

    // 9. Выбираем максимальный
    const maxType = Object.entries(percents).reduce((acc, [type, p]) => {
        return p > percents[acc] ? type : acc;
    }, "Моноактивный"); // дефолт

    return maxType;
}
