/**
 * Проверяет, есть ли хотя бы 1 паттерн каждой категории среди ЯПП
 * @param {string[]} yappNames - массив нормализованных (в нижнем регистре) паттернов
 * @returns {boolean}
 */
function hasAllCategories(yappNames) {
    return Object.values(PATTERN_CATEGORY_MAP).every(catPatterns =>
        catPatterns.some(pat => yappNames.includes(pat))
    );
}

/**
 * Возвращает топ ведущих паттернов среди ЯПП (>=75%) с учётом типа реагирования
 * @param {string[]} yapp - массив ЯПП (оригинальные названия паттернов)
 * @param {string} responseType - тип реагирования (например, "Моноактивный", "Мультиактивный", "Реактивный", "Моноактивный/Реактивный")
 * @param {number} limit - максимальное количество ведущих паттернов (по умолчанию 5)
 * @returns {string[]} массив названий ведущих паттернов
 */
export function getTopPatternsAmongYAPP({ yapp, responseType, limit = 5 }) {
    const yappNames = (yapp || []).map((name) => (name || "").trim().toLowerCase());

    // Проверка полноты категорий и минимального количества ЯПП
    if (!hasAllCategories(yappNames) || yappNames.length <= 5) {
        return [];
    }

    // Получение баллов по таблице для каждого паттерна
    const types = (responseType || "Моноактивный").split("/").map((t) => t.trim());
    const scores = {};
    yappNames.forEach((name, idx) => {
        const patScores = PATTERN_SCORES[name];
        if (!patScores) return;
        // Для смешанного типа берём максимальный балл по всем входящим типам
        const score = Math.max(...types.map((t) => patScores[t] ?? 0));
        scores[name] = { score, idx };
    });

    // Формируем массив c индексами для возврата оригинальных имён
    let resultArr = Object.entries(scores).map(([name, data]) => ({
        name: yapp[data.idx] || name,
        score: data.score,
    }));

    // Сортировка по убыванию баллов, при равенстве — по алфавиту
    resultArr.sort((a, b) => b.score - a.score || a.name.localeCompare(b.name, "ru"));

    // Обрезаем до лимита, если несколько с равным баллом на последнем месте — включаем всех
    if (resultArr.length > limit) {
        const lastScore = resultArr[limit - 1].score;
        return resultArr.filter((p, i) => i < limit || p.score === lastScore).map((p) => p.name);
    }
    return resultArr.map((p) => p.name);
}